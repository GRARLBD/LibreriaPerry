    private boolean existenCamposBlancos() {
        boolean bandera = false;
        String codigo = this.edtCodigo.getText();
        String descripcion = this.edtDescripcion.getText();
        String cantEnt = this.edtStock.getText();
        String precioCosto = this.edtCostoUnitario.getText();
        String precioVenta = this.edtPrecioVenta.getText();
        String undOfer = this.edtUnidOf.getText();
        String acumEnt = edtAcumEntrada.getText();
        String acumSal = edtAcumSalida.getText();

        if (codigo == null || codigo.isEmpty()) {
            bandera = true;
        }
        if (descripcion == null || descripcion.isEmpty()) {
            bandera = true;
        }
        if (cantEnt == null || cantEnt.isEmpty()) {
            bandera = true;
        }
        if (precioCosto == null || precioCosto.isEmpty()) {
            bandera = true;
        }
        if (precioVenta == null || precioVenta.isEmpty()) {
            bandera = true;
        }
        if (undOfer == null || undOfer.isEmpty()) {
            bandera = true;
        }

        if (acumEnt == null || acumEnt.isEmpty()) {
            bandera = true;
        }

        if (acumSal == null || acumSal.isEmpty()) {
            bandera = true;
        }

        return bandera;
    }

    private boolean existenCamposNoHomogeneos() {
        boolean bandera = false;

        try {
            int cantEnt = new Integer(this.edtStock.getText());

            if (cantEnt < 0) {
                bandera = true;
            }
        } catch (Exception e) {
            bandera = true;
        }

        try {
            int undOfert = new Integer(this.edtUnidOf.getText());

            if (undOfert < 1) {
                bandera = true;
            }
        } catch (Exception e) {
            bandera = true;
        }

        try {
            BigDecimal precioCosto = new BigDecimal(this.edtCostoUnitario.getText());
            BigDecimal precioVenta = new BigDecimal(this.edtPrecioVenta.getText());

            if (precioCosto.doubleValue() < 0 || precioVenta.doubleValue() < 0) {
                bandera = true;
            }

            if (precioCosto.doubleValue() >= precioVenta.doubleValue()) {
                JOptionPane.showMessageDialog(this, "El precio de costo es mayor o igual al precio de venta.");
                bandera = true;
            }
        } catch (Exception e) {
            bandera = true;
        }

        try {
            int numEnt = new Integer(this.edtAcumEntrada.getText());
            int numSal = new Integer(this.edtAcumSalida.getText());

            if (numEnt < 0 || numSal < 0) {
                bandera = true;
            }

            if (numSal > numEnt) {
                bandera = true;
            }
        } catch (Exception e) {
            bandera = true;
        }

        return bandera;
    }


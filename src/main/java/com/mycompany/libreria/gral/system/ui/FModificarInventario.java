    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Clave result = Clave.RESULT_SIN_DEFINIR;

        try {
            result = iniciarProc3();
        } catch (Exception e) {
            result = Clave.RESULT_ERROR;
        }

        switch (result) {
            case RESULT_ERROR: {
                Mensaje.mostrarMensaje5(rootPane);
                dispose();
                break;
            }
            case RESULT_ERROR_CAMPOS_INVALIDOS: {
                Mensaje.mostrarMensaje2(this);
                break;
            }
            case RESULT_ERROR_BD: {
                Mensaje.mostrarMensaje3(this);
                dispose();
                break;
            }
            case RESULT_OK: {

                JOptionPane.showMessageDialog(rootPane, "Inventario guardado exitosamente!!");

                try {
                    mainFrame.actualizar();
                } catch (Exception e) {
                }

                if (form == null) {
                    // Hacer nada.
                } else {
                    form.iniciarProc11(Utiles.obtenerValor(edtCodigo));
                }

                if (bandCerrar) {
                    this.dispose();
                } else {
                    this.limpiarFormulario();
                    this.iniciarFormulario();
                }

                break;
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


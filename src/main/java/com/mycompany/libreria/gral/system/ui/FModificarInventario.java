    private Clave iniciarProc1() {

        String codigo = Utiles.obtenerValor(edtCodigo);
        Clave result = Clave.RESULT_OK;

        try {
            if (gestor.consultarInvPorCodigo(codigo)) {
                inv = gestor.obtenerInvPorCodigoM2(codigo);
            }
        } catch (Exception e) {
            e.printStackTrace();
            return Clave.RESULT_ERROR_BD;
        }

        if (inv == null) {
            Utiles.establecerFocus2(edtCodigo);
            Mensaje.mostrarMensajeNoEncontrado(this);
        } else {
            establecerFocus1(edtCodigo);
            Utiles.deshabilitarCampo(edtCodigo);
            habilitarComponentes();
            inicializarComponentes();
            iniciarProc4();
            actualizar();
        }

        return result;
    }

    private Clave iniciarProc2() {
        Clave result = Clave.RESULT_OK;

        try {
            codigo = edtCodigo.getText().trim();
            codigoLote = edtCodigoLote.getText().trim();
            descripcion = edtDescripcion.getText().trim();
            stock = new Integer(edtStock.getText().trim());
            costoUnit = new BigDecimal(edtCostoUnitario.getText().trim());
            precioVenta = new BigDecimal(edtPrecioVenta.getText().trim());
            unidOfert = new Integer(edtUnidOf.getText().trim());
            acumEnt = new Integer(edtAcumEntrada.getText().trim());
            acumSal = new Integer(edtAcumSalida.getText().trim());

            if (codigoBusqCamp.toString().trim().isEmpty()
                    & codigoBusqSP.toString().trim().isEmpty()
                    & codigoBusqOtro.toString().trim().isEmpty()) {
                estadoSeguro = false;
            } else {
                if (cbEstSeguro.isSelected()) {
                    estadoSeguro = true;
                } else {
                    estadoSeguro = false;
                }
            }

            switch (cbEstado.getSelectedIndex()) {
                case 0:
                    estado = false;
                    break;
                case 1:
                    estado = true;
                    break;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return result = Clave.RESULT_ERROR_CAMPOS_INVALIDOS;
        }

        try {
            gestor.guardarInv(codigoBusqCamp.toString(),
                    codigoBusqSP.toString(),
                    codigoBusqOtro.toString(),
                    codigo, codigoLote, descripcion, unidOfert, stock,
                    costoUnit, precioVenta, acumEnt, acumSal, lOfertas, estado, lCodigo, estadoSeguro);
        } catch (Exception e) {
            e.printStackTrace();
            return Clave.RESULT_ERROR_BD;
        }

        return result;
    }

    private Clave iniciarProc3() {
        Clave result = Clave.RESULT_SIN_DEFINIR;

        //  Validaciones
        if (existenCamposBlancos() || existenCamposNoHomogeneos()) {
            return Clave.RESULT_ERROR_CAMPOS_INVALIDOS;
        }

        try {
            result = iniciarProc2();
        } catch (Exception e) {
            e.printStackTrace();
            result = Clave.RESULT_ERROR;
        }

        return result;
    }


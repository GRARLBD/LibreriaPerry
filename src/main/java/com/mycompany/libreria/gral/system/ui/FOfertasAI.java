    private void modificar(Oferta of, int columna, String value) {
       
        switch (columna) {
            case COL_DESC: {
                
                value = Utiles.limpiar(value, "'");
                
                if (value == null || value.isEmpty())
                {
                    // Hacer nada.
                } else
                {
                    of.setDescripcion(value);
                }                
                break;
            }
            case COL_UNID_OFERTADAS: {
                int unidOf;

                try {
                    unidOf = new Integer(value);
                } catch (Exception e) {
                    unidOf = 1;
                }

                if (unidOf < 1) {
                    unidOf = 1;
                }

                of.setUnidOfert(unidOf);
                break;
            }
            case COL_PREC_VEN: {
                BigDecimal precVenta;

                try {
                    precVenta = new BigDecimal(value);
                } catch (Exception e) {
                    precVenta = new BigDecimal(0);
                }

                if (precVenta.doubleValue() < 0) {
                    precVenta = new BigDecimal(0);
                }
                of.setPrecioVenta(precVenta);
                break;
            }
        }
    }

    private Oferta crearNuevaOferta() {
        String value = Oferta.generarCodigo(this.codigo);
        String desc = "nueva oferta.";
        int MIN_UNID_OF = 1;
        boolean EST_ACTIVO = true;
        BigDecimal pVenta = new BigDecimal(0);
        Oferta of = new Oferta();

        of.setDescripcion(desc);
        of.setCodigo(value);
        of.setUnidOfert(MIN_UNID_OF);
        of.setPrecioVenta(pVenta);
        of.setEstado(EST_ACTIVO);

        return of;
    }

    private void iniciarProc2() {

        final String value = Utiles.obtenerCambios(tablaOfertas);
        final String cod = Utiles.obtenerValor(tablaOfertas, COL_CODIGO);

        if (cod == null || cod.isEmpty()) {
        } else {
            if (consultar(copLOfertas, cod)) {

                Oferta of = obtener(copLOfertas, cod);

                modificar(of, tablaOfertas.getSelectedColumn(), value);
                
            }
        }
        actualizarForm();
        Utiles.seleccionarFila(tablaOfertas, tablaOfertas.getSelectedRow());
    }
    
    /**
     * Agrega una oferta a la nueva lista de ofertas.
     *
     * @param o Oferta
     */
    private void agregarALista(List<Oferta> l, Oferta o) {

        if (consultar(l, o.getCodigo())) {
            // Hacer nada.
        } else {
            l.add(o);
        }
    }

    private Clave eliminar(List<Oferta> l, String codigo) {
        Clave result = Clave.RESULT_OK;;

        if (consultar(l, codigo))
        {
            Oferta of = obtener(l, codigo);
            
            l.remove(of);
        } else
        {
            result = Clave.RESULT_NO_EXISTE;
        }

        return result;
    }

    private Oferta obtener(List<Oferta> l, String value) {
        Oferta oferta;

        oferta = null;

        for (Oferta of : l) {
            if (of.getCodigo().equals(value)) {
                oferta = of;
                break;
            }
        }
        return oferta;
    }

    private boolean consultar(List<Oferta> list, String codigo) {
        boolean band;

        band = false;

        for (Oferta of : list) {
            if (of.getCodigo().equals(codigo)) {
                band = true;
                break;
            }
        }

        return band;
    }


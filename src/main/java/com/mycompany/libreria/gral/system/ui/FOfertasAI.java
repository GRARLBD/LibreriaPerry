    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        final int rowSel = tablaOfertas.getSelectedRow();

        //  Validaciones
        if (rowSel < 0) {
            Mensaje.mostrarMensajeSeleccion(this);
        } else {
            String codigo = (String) tablaOfertas.getValueAt(rowSel, COL_CODIGO);

            if (codigo == null || codigo.isEmpty()) {
                // Hacer nada.
                Mensaje.mostrarMensajeSeleccion(rootPane);
            } else {
                Clave result = Clave.RESULT_OK;
                // Acciones
                try {
                    result = eliminar(copLOfertas, codigo);
                } catch (Exception e) {
                    e.printStackTrace();
                    result = Clave.RESULT_ERROR;
                }

                switch (result) {
                    case RESULT_ERROR: {
                        Mensaje.mostrarMensaje5(rootPane);
                        break;
                    }
                    case RESULT_NO_EXISTE: {
                        Mensaje.mostrarMensajeSeleccion(rootPane);
                        break;
                    }
                    case RESULT_OK: {
                        // Hacer nada.
                        break;
                    }
                }
                
                this.actualizarForm();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

